// 2.2 Data Types and Sizes

#include <stdio.h>

int main(){
  
  // integer types
  int i = 12345;
  char c = 'a';

  // short, long variants
  short s = 123;
  long l = 123456789L;
  long ll = 123456789LL;

  // unsigned variants
  unsigned char uc = 255;
  unsigned int ui = 400000u;

  // floating point types
  float f = 3.14f;
  double d = 3.1459;
  long double ld = 3.14159L;
}
